"""
Text Polish — детерминированная пост-обработка ответов ИИ для грамотности.

Применяет ОДНОЗНАЧНЫЕ, безопасные исправления типичных ошибок, которые
допускают LLM при генерации русского текста:

  • Тире: дефис с пробелами с обеих сторон (« - ») → длинное тире (« — »).
    По правилам русской типографики в пояснительных/противительных
    конструкциях ставится тире (U+2014), а не дефис-минус.
  • Кавычки: прямые " " и " " → типографские «ёлочки».
  • Многоточие: три точки «...» → единый символ «…».
  • Пробелы: двойные/тройные → одинарный; пробел перед пунктуацией убирается;
    после пунктуации добавляется, если его нет.
  • Множественные знаки: «!!!» → «!», «???» → «?», «...!!» → «!».
  • Лишние пробелы в начале/конце строк, вокруг переносов строки.
  • Пробел перед запятой/точкой/двоеточием/точкой с запятой убирается.
  • Отсутствие пробела после запятой/точки/двоеточия (между словом и словом)
    добавляется — но НЕ внутри чисел (1,5) и НЕ внутри URL/HTML-тегов.

ВАЖНО — чего тут НЕТ (намеренно):
  • Орфограммы «чтобы / что бы», «также / так же», «тоже / то же» —
    контекстно-зависимы, авто-исправление опасно. Они регулируются промптом.
  • Запятые при причастных/деепричастных оборотах — требуется синтаксис,
    автоматика здесь недопустима.
  • Изменение слов, перестановка фраз — только типографика и пунктуация.

Функция `polish_grammar` вызывается из ai/router.py:_clean_response для ВСЕХ
ответов ИИ (посты канала, чаты, комментарии) и из masha.py:_clean_post_text.
Применяется ДО linkify (чтобы тире/кавычки не ломали HTML-теги) и ПОСЛЕ
удаления markdown.
"""

import re
import logging

logger = logging.getLogger("dasha.text_polish")


# ──────────────────────────────────────────────────────────────────────────────
# 1. ТИРЕ: дефис-минус с пробелами → длинное тире
# ──────────────────────────────────────────────────────────────────────────────

# Дефис-минус (-, U+002D) окружённый пробелами с обеих сторон → тире (—, U+2014).
# Также ловим дефис в начале строки после переноса ("текст\n- продолжение").
# ВАЖНО: используем [ \t]+ (не \s+), чтобы НЕ пересекать переносы строки —
# иначе "слово\n- пункт" превратится в "слово — пункт" и потеряется перенос.
_RE_DASH_SPACED = re.compile(r'[ \t]+[-–—][ \t]+')  # дефис с пробелами → тире
# Дефис/тире в начале строки (список) → нормализуем на —
_RE_DASH_LINE_START = re.compile(r'((?:^|\n)[ \t]*)[-–—]([ \t]+)')
# Дефис между словами БЕЗ пробелов ("что-то", "как-нибудь") НЕ трогаем — это частица.


def _fix_dashes(text: str) -> str:
    """Дефис с пробелами → длинное тире; дефис в начале строки (список) → тире."""
    # " - " / " – " / " — " → " — " (только в пределах одной строки)
    text = _RE_DASH_SPACED.sub(' — ', text)
    # Списки в начале строки: "- пункт" / "— пункт" / "– пункт" → "— пункт"
    # (нормализуем любой из -/–/— на —, не дублируем)
    text = _RE_DASH_LINE_START.sub(r'\1—\2', text)
    return text


# ──────────────────────────────────────────────────────────────────────────────
# 2. КАВЫЧКИ: прямые " " и " " → «ёлочки»
# ──────────────────────────────────────────────────────────────────────────────

# Парные кавычки. Идём по тексту, отслеживаем «открыта/закрыта».
_RE_STRAIGHT_QUOTE = re.compile(r'["“”"]')
_RE_GUILLEMET_OPEN = re.compile(r'«')
_RE_GUILLEMET_CLOSE = re.compile(r'»')


def _fix_quotes(text: str) -> str:
    """Преобразовать прямые двойные кавычки в типографские «ёлочки».

    Прямые кавычки " заменяются парно: первая → «, вторая → », третья → « и т.д.
    Уже существующие « » не трогаются. Типографские " " тоже нормализуются.
    """
    if '"' not in text and '"' not in text and '"' not in text and '"' not in text:
        return text
    out = []
    open_now = True  # следующая прямая кавычка — открывающая
    for ch in text:
        if ch in ('"', '"', '"', '"'):
            out.append('«' if open_now else '»')
            open_now = not open_now
        else:
            out.append(ch)
    return ''.join(out)


# ──────────────────────────────────────────────────────────────────────────────
# 3. МНОГОТОЧИЕ: три точки → единый символ …
# ──────────────────────────────────────────────────────────────────────────────

_RE_ELLIPSIS = re.compile(r'(?<!\.)\.{3,}(?!\.)')  # 3+ точек, не часть 4+ точек


def _fix_ellipsis(text: str) -> str:
    """Три и более точки подряд → … (но не задеваем 4+ точек, которые могли быть осмысленно)."""
    # Заменяем ровно 3+ точек на одно многоточие
    text = re.sub(r'\.{3,}', '…', text)
    return text


# ──────────────────────────────────────────────────────────────────────────────
# 4. ПРОБЕЛЫ И ПУНКТУАЦИЯ
# ──────────────────────────────────────────────────────────────────────────────

# Пробел перед пунктуацией: "слово ," → "слово,"
_RE_SPACE_BEFORE_PUNCT = re.compile(r'[ \t]+([,.;:!?])')
# Отсутствие пробела после пунктуации между словами: "слово,слово" → "слово, слово"
# Но НЕ разрываем: числа (1,5 / 1.000), URL (http://), время (12:30), даты (2024.01.01),
# телефоны (+7 913 448-37-17), HTML-теги (<a href="...">), IP (192.168.0.1).
# Для : исключаем / (URL http://) и \d (время 12:30, числа 1:2).
# Для . исключаем случаи, когда точка внутри домена/версии (site.com, v1.2.3).
_RE_NO_SPACE_AFTER_COMMA = re.compile(
    r',(?=[^\s\d,;:!?)"\]\u00BB«\n./])'
)
_RE_NO_SPACE_AFTER_SEMICOLON = re.compile(
    r';(?=[^\s\d,;:!?)"\]\u00BB«\n])'
)
_RE_NO_SPACE_AFTER_COLON = re.compile(
    r':(?=[^\s\d,;:!?)"\]\u00BB«\n/])'  # / исключает URL http://
)
_RE_NO_SPACE_AFTER_PERIOD = re.compile(
    r'\.(?=[^\s\d.])'  # базово: не после цифры и не перед точкой
)
_RE_NO_SPACE_AFTER_EXCL = re.compile(
    r'(?<=!)(?=[^\s)\]"’»\n])'
)
_RE_NO_SPACE_AFTER_QUES = re.compile(
    r'(?<=\?)(?=[^\s)\]"’»\n])'
)


def _fix_spaces_and_punct(text: str) -> str:
    """Нормализация пробелов вокруг пунктуации.

    Защита URL/доменов: точки внутри доменных имён (site.com, abakanmebel.online)
    НЕ разрываем пробелом. Для этого исключаем случаи, когда точка стоит между
    латинскими буквами и после — снова буквы/точки (паттерн домена).
    """
    # 1. Убираем пробел(ы) перед пунктуацией (только в пределах строки)
    text = _RE_SPACE_BEFORE_PUNCT.sub(r'\1', text)
    # 2. Добавляем пробел после , ; : если за ним идёт буква
    text = _RE_NO_SPACE_AFTER_COMMA.sub(', ', text)
    text = _RE_NO_SPACE_AFTER_SEMICOLON.sub('; ', text)
    text = _RE_NO_SPACE_AFTER_COLON.sub(': ', text)
    # 3. Точка: добавляем пробел, КРОМЕ случаев:
    #    - точка между латинскими буквами (домен/файл: site.com, file.txt)
    #    - точка в версии (v1.2.3)
    #    Для этого: заменяем потенциальные домены на placeholder, добавляем пробел,
    #    возвращаем домены.
    # Прячем URL и доменные имена
    placeholders = []

    def _hide(m):
        placeholders.append(m.group(0))
        return f"\x01DOM{len(placeholders)-1}\x01"

    # URL целиком
    text = re.sub(r'https?://[^\s<>"]+', _hide, text)
    # Домены: слово.слово.слово... (последняя часть — буквы, TLD)
    text = re.sub(r'\b[a-zA-Z][a-zA-Z0-9-]*(?:\.[a-zA-Z][a-zA-Z0-9-]*)+\b', _hide, text)
    # Версии: v1.2.3 / 1.2.3
    text = re.sub(r'\b[vv]?\d+(?:\.\d+)+\b', _hide, text)
    # e-mail
    text = re.sub(r'[\w.+-]+@[\w.-]+\.\w+', _hide, text)

    # Теперь безопасно добавляем пробел после точки
    text = _RE_NO_SPACE_AFTER_PERIOD.sub('. ', text)

    # Возвращаем домены/URL
    for i, p in enumerate(placeholders):
        text = text.replace(f"\x01DOM{i}\x01", p)

    # 4. Добавляем пробел после ! ? если за ним идёт буква
    #    (lookbehind+lookahead нулевой ширины → вставляем пробел)
    text = _RE_NO_SPACE_AFTER_EXCL.sub(' ', text)
    text = _RE_NO_SPACE_AFTER_QUES.sub(' ', text)
    # 5. Двойные/тройные пробелы → одинарный
    text = re.sub(r'[ \t]{2,}', ' ', text)
    # 6. Пробелы в начале/конце каждой строки
    text = '\n'.join(line.strip() for line in text.split('\n'))
    return text


# ──────────────────────────────────────────────────────────────────────────────
# 5. МНОЖЕСТВЕННЫЕ ЗНАКИ
# ──────────────────────────────────────────────────────────────────────────────

# «!!!» → «!», «???» → «?», «!!!?» → «!?» (сохраняем «!?» и «?!» как осмысленные)
_RE_MULTI_EXCL = re.compile(r'!{2,}')
_RE_MULTI_QUES = re.compile(r'\?{2,}')


def _fix_multiple_marks(text: str) -> str:
    """Схлопываем множественные !! и ?? в один знак (кроме осмысленных ?! и !?)."""
    text = _RE_MULTI_EXCL.sub('!', text)
    text = _RE_MULTI_QUES.sub('?', text)
    return text


# ──────────────────────────────────────────────────────────────────────────────
# 6. ПЕРЕНОСЫ И ПУСТЫЕ СТРОКИ
# ──────────────────────────────────────────────────────────────────────────────

def _fix_newlines(text: str) -> str:
    """Схлопываем 3+ переносов в 2 (один пустая строка между абзацами)."""
    text = re.sub(r'\n{3,}', '\n\n', text)
    # Убираем пробельные строки (только пробелы/табы) — превращаем в пустые
    text = '\n'.join(line.rstrip() if line.strip() == '' else line for line in text.split('\n'))
    return text


# ──────────────────────────────────────────────────────────────────────────────
# 7. ТОЧКА В КОНЦЕ + ЛИШНИЕ ПРОБЕЛЫ ВОКРУГ
# ──────────────────────────────────────────────────────────────────────────────

def _fix_trailing(text: str) -> str:
    """Финальная подчистка: пробел перед многоточием, точка-многоточие и т.п."""
    # " ." → "…"? Нет — " ." уже обработано выше. Тут — точка перед многоточием:
    text = re.sub(r'\.\.\.\B', '…', text)  # ".." остатки
    # Многоточие + точка = просто многоточие
    text = re.sub(r'…\.', '…', text)
    return text


# ──────────────────────────────────────────────────────────────────────────────
# ЗАЩИТА HTML — не трогать содержимое тегов <a href="..."> и <...>
# ──────────────────────────────────────────────────────────────────────────────

# Если текст УЖЕ содержит HTML (после _linkify), мы НЕ должны ломать
# <a href="...">phone</a>. Поэтому polish_grammar вызывается ДО _linkify_contacts
# (на «чистый» текст без HTML-тегов). Но на всякий случай — защита от уже-HTML:
# вырезаем href-значения в кавычках, обрабатываем текст, возвращаем обратно.

_RE_HREF_ATTR = re.compile(r'(href|src|tel)\s*=\s*"([^"]*)"')


def _protect_html_attrs(text: str):
    """Вернуть (text_with_placeholders, map) — спрятать href="..." значения."""
    placeholders = []

    def _repl(m):
        placeholders.append(m.group(0))
        return f"\x00ATTR{len(placeholders)-1}\x00"

    protected = _RE_HREF_ATTR.sub(_repl, text)
    return protected, placeholders


def _restore_html_attrs(text: str, placeholders) -> str:
    for i, p in enumerate(placeholders):
        text = text.replace(f"\x00ATTR{i}\x00", p)
    return text


# ──────────────────────────────────────────────────────────────────────────────
# ОСНОВНАЯ ФУНКЦИЯ
# ──────────────────────────────────────────────────────────────────────────────

def polish_grammar(text: str) -> str:
    """Применить все детерминированные исправления грамотности/типографики.

    Безопасно для HTML: прячем href="..." атрибуты, обрабатываем текст,
    возвращаем обратно. Применять ПОСЛЕ удаления markdown, ДО linkify.
    """
    if not text:
        return text

    # Защита HTML-атрибутов (на случай если текст уже содержит <a href="...">)
    text, placeholders = _protect_html_attrs(text)

    # Порядок важен: сначала структурные исправления, потом пробелы
    text = _fix_dashes(text)
    text = _fix_quotes(text)
    text = _fix_ellipsis(text)
    text = _fix_multiple_marks(text)
    text = _fix_spaces_and_punct(text)
    text = _fix_newlines(text)
    text = _fix_trailing(text)

    # Восстанавливаем HTML-атрибуты
    text = _restore_html_attrs(text, placeholders)

    return text


# ──────────────────────────────────────────────────────────────────────────────
# САМОПРОВЕРКА (вызывается из теста, не из прод-кода)
# ──────────────────────────────────────────────────────────────────────────────

def _self_test() -> bool:
    """Быстрая самопроверка основных кейсов. Возвращает True если всё ок."""
    cases = [
        # (input, expected_substring_in_output)
        ("Кухня - это важно", "Кухня — это важно"),
        ("Стиль «лофт» - это", "Стиль «лофт» — это"),
        ("Привет ,как дела", "Привет, как дела"),
        ("Слово.Слово", "Слово. Слово"),
        ("Да!!! Конечно???", "Да! Конечно?"),
        ("Ого...", "Ого…"),
        ('Он сказал "привет"', "Он сказал «привет»"),
        ("Много   пробелов", "Много пробелов"),
        ("Слово , слово", "Слово, слово"),
        ("1,5 метра", "1,5 метра"),  # число НЕ разрывать
        ("12:30", "12:30"),  # время без следующей буквы НЕ разрывать
        ("http://site.com", "http://site.com"),  # URL НЕ разрывать
        ("+7 913 448-37-17", "+7 913 448-37-17"),  # телефон НЕ трогать
        ("— Привет! — сказала она.", "— Привет! — сказала она."),  # тире не дублируется
        ("- пункт один\n- пункт два", "— пункт один\n— пункт два"),  # список
        ("что-то, как-нибудь", "что-то, как-нибудь"),  # дефис в частицах НЕ трогать
    ]
    ok = True
    for inp, expected in cases:
        result = polish_grammar(inp)
        if expected not in result:
            logger.debug(f"polish case FAIL: {inp!r} -> {result!r} (expected {expected!r})")
            ok = False
    return ok


if __name__ == "__main__":
    # Запуск: python3 -m bot.text_polish
    import sys
    samples = [
        "Кухня - это сердце дома ,а шкаф-купе - его лицо.",
        'Он сказал "да"!!! И добавил что-то ещё...',
        "Слово.Слово,слово   слово.",
        "Время 12:30 , цена 1,5 тыс руб.",
        "— Привет! — сказала она.",
    ]
    for s in samples:
        print(f"IN : {s}")
        print(f"OUT: {polish_grammar(s)}")
        print()
    print("Self-test:", "PASS" if _self_test() else "FAIL")
    sys.exit(0 if _self_test() else 1)
